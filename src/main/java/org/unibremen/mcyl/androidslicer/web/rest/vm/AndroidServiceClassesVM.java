package org.unibremen.mcyl.androidslicer.web.rest.vm;

/**
 * View Model object for storing android service classes.
 */
public class AndroidServiceClassesVM {

    private String name;

    private String path;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
}